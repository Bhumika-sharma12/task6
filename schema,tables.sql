create database Orders;
use Orders;

CREATE TABLE orders (
    order_id INT,
    product_id INT,
    product_name TEXT,
    order_date DATE,
    ship_date DATE
);
-- CREATE TABLE: order_details
CREATE TABLE order_details (
    product_id INT,
    order_date DATE,
    price NUMERIC(10, 2),
    quantity INT,
    revenue NUMERIC(10, 2),
    order_volume INT
);

